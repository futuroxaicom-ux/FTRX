// Smart answer matching function with multilingual support
export const findAnswer = (question, language = 'en') => {
  const knowledge = chatKnowledge[language] || chatKnowledge.en;
  const lowerQuestion = question.toLowerCase().trim();
  
  // Multilingual greetings
  const greetings = ['hi', 'hello', 'hey', 'yo', 'sup', 'howdy', 'hola', 'cześć', 'siema', 'czesc'];
  const thanks = ['thanks', 'thank you', 'thx', 'ty', 'tysm', 'gracias', 'dziękuję', 'dzięki', 'dziekuje'];
  const goodbye = ['bye', 'goodbye', 'see you', 'cya', 'bb', 'adios', 'pa', 'do widzenia'];
  const confirmations = ['ok', 'okay', 'k', 'kk', 'alright', 'got it', 'cool', 'nice'];
  
  const responses = {
    thanks: {
      en: "You're welcome! 😊 Is there anything else you'd like to know about FuturoX AI?",
      es: "¡De nada! 😊 ¿Hay algo más que quieras saber sobre FuturoX AI?",
      pl: "Nie ma za co! 😊 Czy jest coś jeszcze o co chciałbyś zapytać?"
    },
    goodbye: {
      en: "Goodbye! 👋 Feel free to come back anytime. Have a great day!",
      es: "¡Adiós! 👋 Vuelve cuando quieras. ¡Que tengas un gran día!",
      pl: "Do widzenia! 👋 Wróć kiedy chcesz. Miłego dnia!"
    },
    confirmation: {
      en: "Great! 👍 Do you have any other questions about FuturoX AI?",
      es: "¡Genial! 👍 ¿Tienes alguna otra pregunta sobre FuturoX AI?",
      pl: "Świetnie! 👍 Masz jakieś inne pytania o FuturoX AI?"
    },
    idk: {
      en: "No worries! Try asking:\n• What is FuturoX AI?\n• How to buy FTRX?\n• When is the launch? 😊",
      es: "¡No te preocupes! Intenta preguntar:\n• ¿Qué es FuturoX AI?\n• ¿Cómo comprar FTRX?\n• ¿Cuándo es el lanzamiento? 😊",
      pl: "Nie martw się! Spróbuj zapytać:\n• Co to jest FuturoX AI?\n• Jak kupić FTRX?\n• Kiedy uruchomienie? 😊"
    }
  };
  
  if (greetings.some(greeting => lowerQuestion === greeting || lowerQuestion.startsWith(greeting + ' '))) {
    return getGreeting(language);
  }
  
  if (thanks.some(thank => lowerQuestion.includes(thank))) {
    return responses.thanks[language] || responses.thanks.en;
  }
  
  if (goodbye.some(bye => lowerQuestion.includes(bye))) {
    return responses.goodbye[language] || responses.goodbye.en;
  }
  
  if (confirmations.some(conf => lowerQuestion === conf)) {
    return responses.confirmation[language] || responses.confirmation.en;
  }
  
  if (lowerQuestion.includes('idk') || lowerQuestion.includes("don't know") || lowerQuestion.includes('no sé') || lowerQuestion.includes('nie wiem')) {
    return responses.idk[language] || responses.idk.en;
  }
  
  // Check FAQ for keyword matches
  for (const faq of knowledge.faqs) {
    for (const keyword of faq.keywords) {
      if (lowerQuestion.includes(keyword.toLowerCase())) {
        return faq.answer;
      }
    }
  }
  
  return knowledge.fallback;
};

export const getGreeting = (language = 'en') => {
  const knowledge = chatKnowledge[language] || chatKnowledge.en;
  return knowledge.greetings[Math.floor(Math.random() * knowledge.greetings.length)];
};

export const getQuickReplies = (language = 'en') => {
  const knowledge = chatKnowledge[language] || chatKnowledge.en;
  return knowledge.quickReplies;
};
